let btn_scroll = document.querySelector('.btn_scroll')
    
     // Button Scroll

  window.onscroll = function () {
    
    if (window.screen.width >= 1280) {
      scrollY <= 815
      ? document.querySelector('.header-js').classList.remove('header-bg')
      : document.querySelector('.header-js').classList.add('header-bg');
      
    } else {
      scrollY <= 120
      ? document.querySelector('.header-js').classList.remove('header-bg')
      : document.querySelector('.header-js').classList.add('header-bg');
      
    }
      // Scroll to Top                                                      
      
    if (window.pageYOffset > 200) {
    btn_scroll.style.opacity = '1'
    } else { btn_scroll.style.opacity = '0' }
    
  };